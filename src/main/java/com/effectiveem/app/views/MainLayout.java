import com.effectiveem.app.data.entity.User;
import com.effectiveem.app.security.AuthenticatedUser;
import com.effectiveem.app.views.about.AboutView;
import com.effectiveem.app.views.gridwithfilters.GridwithFiltersView;
import com.effectiveem.app.views.helloworld.HelloWorldView;
import com.effectiveem.app.views.register.RegisterView;
import com.vaadin.flow.component.Component;
import com.vaadin.flow.component.applayout.AppLayout;
import com.vaadin.flow.component.avatar.Avatar;
import com.vaadin.flow.component.contextmenu.MenuItem;
import com.vaadin.flow.component.html.Anchor;
import com.vaadin.flow.component.html.Div;
import com.vaadin.flow.component.html.H1;
import com.vaadin.flow.component.html.Header;
import com.vaadin.flow.component.html.Image;
import com.vaadin.flow.component.html.Nav;
